                <h2 class="titulo-post">Como configurar um ambiente de desenvolvimento JAVA</h2>
                <div class="postagem">
                    <div class="imagem-destaque">
                        <img class="responsive-img" src="http://farm6.staticflickr.com/5614/15602332537_bae1aaccd8_b.jpg">
                    </div>
                    
                    <div class="info-post">
                        <span class="col s12 m3 l3" style="padding:0;"><strong>Data:</strong> 12/02/2018</span>
                        <span class="col s12 m9 l9" style="padding:0;"><strong>Autor:</strong> Douglas Siqueira</span>
                        <div style="clear:both;"></div>
                    </div>
                    <div class="conteudo-post">
                        <p style="text-align: justify;">Este novo avanço da tecnologia pode ser implantado no interior do olho para recuperar a visão perdida por lesões da córnea.</p>
                        <p style="text-align: justify;">O método, que até agora tem sido demonstrado com sucesso em testes com animais, pode ter o potencial de aumentar dramaticamente o acesso a transplantes de córnea – o que poderia mudar a vida de cerca de 10 milhões de pessoas em todo o mundo.</p>
                        <h2>Palavra do Engenheiro Biomédico</h2>
                        <blockquote>
                        <p style="text-align: justify;">“Acreditamos que o nosso novo tratamento funciona melhor do que uma córnea doada, e esperamos que no futuro possamos usar as células do próprio paciente, reduzindo o risco de rejeição”, diz o engenheiro biomédico Berkay Ozcelik, que liderou a pesquisa, enquanto na Universidade de Melbourne. “Mais estudos são necessários, mas esperamos ver o tratamento testado em pacientes no próximo ano.”</p>
                        </blockquote>
                        <h2>Córnea</h2>
                        <p style="text-align: justify;">A córnea é a camada mais externa do olho. Para manter saudável, ela precisa ficar úmida e transparente, mas o envelhecimento, e trauma podem levar a danos na córnea, como inchaço, o que resulta na deterioração da visão.</p>
                        <p style="text-align: justify;"><img class="aligncenter responsive-img" title="película para olhos" src="http://www.megacuriosidades.net/wp-content/uploads/2016/08/película-para-olhos.jpg" alt="recuperar a visão"></p>
                        <p style="text-align: justify;">Transplantes de córnea são atualmente a forma mais eficaz de restaurar a visão perdida a danos na córnea, mas há uma falta significativa de córneas de doadores. Mais de 47.000 transplantes de córnea ocorreram nos EUA em 2014, mas não há o suficiente tecido do doador para satisfazer a demanda global.<br>
                        Há também a possibilidade de rejeição de tecido de dadores de córnea, e a necessidade de tomar esteroides para lutar contra a rejeição, além de outras complicações.</p>
                        <h2>Riscos</h2>
                        <blockquote>
                        <p style="text-align: justify;">“O problema com o tecido do doador é que todo o processo de manipulação, colhendo as células dos pacientes, armazenamento e depois transplantá-los pode ter efeitos prejudiciais sobre as próprias células”, Ozcelik explica à Léa Surugue no International Business Times. “Há um risco potencial para a transmissão da doença e risco de rejeição de tecidos, desde que você tenha um transplante de um corpo estranho.”</p>
                        </blockquote>
                        <h2>Solução</h2>
                        <p style="text-align: justify;">A película invisível da equipe de Ozcelik desenvolveu o que poderia contornar esses problemas. A técnica, que até agora tem sido testada em ovelhas, envolve tomar uma amostra de células da córnea do sujeito, cultivando-os no filme sintético, e, em seguida, devolvê-los em maior número para o olho – onde as células regeneradas restauram as funções de umidade de bombeamento que mantem a córnea saudável e clara.</p>
                        <blockquote>
                        <p style="text-align: justify;">“A película de hidrogel que desenvolvemos nos permite crescer uma camada de células da córnea no laboratório”, diz Berkay num comunicado de imprensa. “Em seguida, pode-se implantar essa película sobre a superfície interna da córnea de um paciente, dentro do olho, através de uma incisão muito pequena.”</p>
                        </blockquote>
                        <h2>Mais fina que um Fio de cabelo</h2>
                        <p style="text-align: justify;">A película de hidrogel é mais fina do que um fio de cabelo humano com apenas 50 micrômetros, e uma vez que as células implantadas tem restaurado a saude da água fluindo entre a córnea e interiores do olho, o filme começa a degradar-se, desaparecendo em dois meses.</p>
                        <h2>Usos e Vantagens</h2>
                        <blockquote>
                        <p style="text-align: justify;">“Estes materiais mostram inflamação mínima, não causam problemas adversos em tudo e pode causar a regeneração dos tecidos, portanto, o que nos permite usar isso para várias aplicações”, disse Ozcelik Cheryl Hall, no <a href="http://www.abc.net.au/news/2016-08-15/cornea-cells-'successfully'-grown-and-implanted-cure-blindness/7736240" target="_blank">ABC</a>.</p>
                        </blockquote>
                        <p style="text-align: justify;">Além de remover os riscos de transmissão de doenças ou rejeição dos tecidos que acompanham o transplante de córnea convencionais, uma outra vantagem do implante da película poderia cultivar células da córnea saudáveis para uso em vários recipientes.</p>
                        <blockquote>
                        <p style="text-align: justify;">“A outra vantagem da nossa técnica é que, mesmo se você não usar as próprias células dos pacientes, porque podemos regenerar e aumentar o número de células de um doador em cultura, poderíamos usar o material de córnea de um doador para talvez, digamos, 20 pacientes “, disse Ozcelik International Business Times.</p>
                        </blockquote>
                        <h2>Testes em Coelhos</h2>
                        <p style="text-align: justify;">Não é a primeira vez que vimos os cientistas usarem esses métodos para restaurar as funções da córnea saudáveis. No início do ano, pesquisadores do <a href="http://www.megacuriosidades.net/tags/japao/">Japão </a>e do Reino Unido descobriram que as células da córnea humana poderiam ser implantadas nos olhos de coelhos com lesões na córnea e recuperar a visão.</p>
                        <p style="text-align: justify;">Ozcelik agora espera começar os testes clínicos com seu filme hidrogel no próximo ano, e enquanto é muito cedo para dizer se os resultados positivos em animais pode ser reproduzido com as pessoas, é incrível pensar nas possibilidades que esta técnica poderia ter.<br>
                        Nós não podemos esperar para ver onde essa investigação pode chegar.</p>


